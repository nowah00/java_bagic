package Day4;

public class IfEx02 {
    public static void main(String[] args) {
        // Score 변수에 93점을 저장
        int score = 93;

        // 현재 Score 변수의 값의 90점 이상이면 A, 미만이면 Fail
        if (score >= 90){
            System.out.println("A");
        } else {
            System.out.print("Fail");
        }

























    }
}
