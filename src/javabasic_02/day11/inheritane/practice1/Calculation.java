public class Calculation {

    void addition(int n1, int n2){
        int result = n1 + n2;
        System.out.println("두 수의 덧셈: " + result);
    }
    void subtraction(int n1, int n2){
        int result = n1 - n2;
        System.out.println("두 수의 뺄셈: " + result);
    }
}
