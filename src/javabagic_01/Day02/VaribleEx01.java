package javabagic_01.Day02;

public class VaribleEx01 {
    public static void main(String[] args) {
        // 1. 정수형 변수 value 를 생성하세요.
        int value; // 변수를 선언시 데이터 타입 변수이름; 선언한다.

        // 2. 변수 value 에 20을 저장하세요.
        value = 20;  // 변수 = 숫자; 가 변수에 숫자를 저장한다.

        // 3. result 에 value 변수에 30을 더한 후 저장하세요.
        int result = value + 30;

        // 4. 변수 result 의 값을 콘솔에 출력하세요.
        System.out.println(result);

    }
}
// 변수는 데이터타입 변수명으로 선언한 뒤 변수는 초기화가 필요하다.